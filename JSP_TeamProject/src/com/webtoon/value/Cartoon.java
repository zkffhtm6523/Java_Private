package com.webtoon.value;

public class Cartoon {
	private String img;
	private String title;
	private String wri_story;
	private String wri_drawing;
	private String story;
	private String genre;
	private String link;
	
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWri_story() {
		return wri_story;
	}
	public void setWri_story(String wri_story) {
		this.wri_story = wri_story;
	}
	public String getWri_drawing() {
		return wri_drawing;
	}
	public void setWri_drawing(String wri_drawing) {
		this.wri_drawing = wri_drawing;
	}
	public String getStory() {
		return story;
	}
	public void setStory(String story) {
		this.story = story;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	
	
} 
